// Data Types in Java

/* Integer Data Type.
 * These data types represent integer numbers i.e numbers without any fractional parts or decimal points.
 * Like ==> 125, -225678,0,1022
 * Integer data types are again subdivided into bytes,short,int,and long types
 *
 * Data type             Memory size            Minimum and Maximum Values
 *   byte                  1 byte                     -128 to +128
 *   short                 2 bytes                    -32768 to +32767
 *   int                   4 bytes                    -2147483648 to +2147483648
 *   long                  8 bytes                    -9223372036854775808 to +9223372036854775808
 */

//-------------------------------------------------------------------------------------------------------

/* Float Data Type.
 * These data types are useful to represent numbers with decimal points.
 * For example ==> 3.14,0.0012,-123.11
 * These are again classified as float(Single precision floating point number) and double (double precision floating
 * point number).
 *
 * Data type             Memory size            Minimum and Maximum Values
 *  float                  4 bytes              -3.4e38 to -1.4e-45 for negative values
 *                                               and 1.4e-45 to 3.4e38 for positive values
 *  double                 8 bytes              -1.8e308 to -4.9e-324 for negative values
 *                                               and 4.9e-324 to 1.8e308 for positive values
 */

//-------------------------------------------------------------------------------------------------------

/* Character Data Type.
 * This data type represents a single character.
 * Like ==> 'a','P','&','*',etc
 *
 * Data type             Memory size            Minimum and Maximum Values
 * char                    2 bytes                     0 to 65535
 */

//-------------------------------------------------------------------------------------------------------

/* String Data Types.
 * A String represents a group of characters.
 * Like "New Delhi","AP123",etc.
 */

//-------------------------------------------------------------------------------------------------------

/* Boolean Data Types.
 * Boolean data types represent any of the two values - true or false. JVM uses 1 bit to represent a boolean
 * value internally.
 * For examples ==> boolean result = false;
 */
