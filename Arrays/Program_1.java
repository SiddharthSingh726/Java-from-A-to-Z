// Write a program to create a 1D array and read its elements by using a loop and display them one by one.
package Arrays;

public class Program_1 {
    public static void main(String[] args) {
        int []arr = {50,60,55,67,70};
        for(int i=0;i<arr.length;i++){
            System.out.println("The value at index "+i+" is = "+arr[i]);
        }
    }
}
