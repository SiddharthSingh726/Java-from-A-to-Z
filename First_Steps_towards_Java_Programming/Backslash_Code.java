// This Code will help in Learning the importance of Backslash Code.
package First_Steps_towards_Java_Programming;

import java.util.*;

public class Backslash_Code {
    public static void main(String []args){
        // 1 ----> \n ==> Next line.
        int a = 10, b = 25;
        int z = a+b;
        System.out.println("Sum of the two nummbers = \n"+z); // we used next line by using \n.

        // 2 -----> \t ===> Horizontal tab space.
        System.out.println(a+"\t"+b); // It created a one tab space by using \t.

        System.out.println(":"+3); // Using + to concatenate.

        System.out.println("Hello \\ Hi \" "+b); // Here \ is helping to print the signs.

    }
}
