package Inheritance;

class Student {
    int id;
    String name;
    String address;
    int marks;
    public void  setId(int id) {
        this.id = id;
    }
    int getId() {
        return id;
    }
    public void  setName(String name) {
        this.name = name;
    }
    String getName() {
        return name;
    }
    public void  setAddress(String address) {
        this.address = address;
    }
    String getAddress() {
        return address;
    }
    public void  setMarks(int marks) {
        this.marks = marks;
    }
    int getMarks() {
        return marks;
    }
}
